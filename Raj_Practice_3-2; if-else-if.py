#Pick a random number between 1-7 by using if-else-if statements.
number = int(input('Enter a number from 1-7: '))

if number == 1:

    print('Monday')

else:

    if number == 2:

        print('Tuesday')

    if number == 3:

        print('Wednesday')

    if number == 4:

        print('Thursday')

    if number == 5:

        print('Friday')

    if number == 6:

        print('Saturday')

    if number == 7:

        print('Sunday')

    if number < 1 or number > 7:

        print('Error!')
    
