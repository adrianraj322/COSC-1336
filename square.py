#Create two functions (One for a square's area and another for its perimeter) that hold its formulas and prepare it to be imported.

#Create a function for the square's area with its formula.
def square_area(square_side):
    area = square_side ** 2
    return area

#Create a function for the square's perimeter with its formula.
def square_perimeter(square_side):
    perimeter = square_side * 4
    return perimeter
    
